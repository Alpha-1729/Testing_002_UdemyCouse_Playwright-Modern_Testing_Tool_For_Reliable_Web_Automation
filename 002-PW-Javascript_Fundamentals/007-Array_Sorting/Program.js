let fruits = ["banana", "apple", "orange"];

fruits.sort();

console.log(fruits);

let scores = ["1", "008", "5", "6"];

// Implementing custom sort.
scores.sort((a, b) => a - b);
console.log(scores);

// Reversing an array.
scores.reverse();
console.log(scores);
