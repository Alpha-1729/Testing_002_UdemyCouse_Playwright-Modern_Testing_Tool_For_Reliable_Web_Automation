// if else-if and else condition.
let age = 23;

if (age < 10) {
    console.log("You are  child.");
} else if (age < 18) {
    console.log("You are young.");
} else {
    console.log("You are old.");
}

// While loop.
let i = 0;
while (i <= 10) {
    console.log(i); 
    i++;
}

// Do while loop.
i = 0;
do {
    console.log("Hello");
} while (i > 0);

// For loop
for (let i = 0; i <= 10; i++) {
    console.log(i);
}
